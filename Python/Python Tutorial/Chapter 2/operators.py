# Arithmetic Operator
print("The value of 3+4 =",3+4)
print("The value of 3-4 =",3-4)
print("The value of 3*4 =",3*4)
print("The value of 3/4 =",3/4)
print("The value of 16%4 =",16%4)

# Assignment Operators
c=34
c+=2    # c=c+2 gives c=36
c-=29   # c=c-2 gives c=7
c*=2    # c=c*2 gives c=14
c/=2    # c=c/2 gives c=7.0
print(c)

# Comparison Operators
print(a<b)
print(a<=b)

print(a>b)
print(a>=b)

print(a==b)
print(a!=b)

# Logical Operators
bool1 = True
bool2 = False
print("The value of bool1 and bool2 is",(bool1 and bool2))
print("The value of bool1 or bool2 is",(bool1 or bool2))
print("The value of not bool2 is",(not bool2))
